"""
4 Operator in python
1. Assignment operator
2. Arithmetic operator
3. Logical operator
4. Comparison operator
"""

# Assignment Operators
number1 = 10
#number1 += 1
#number1 -= 1

# Arithmetic Operators
result = number1 + number1 # Addition 20
print(result)
result = result - number1 # Subtraction 10
print(result)
result = result * result # Multiplication 100
print(result)
result = result / result # Divide
print(result)
result = number1 % 10 # Modulo 0
print(result)
result = number1 // 12 # Floor Divide 0
print(result)
result2 = 5 ** 2 # Exponentiation 1
print(result2)

# Comparison Operators
print(10 == 10)  # Equals
print(10 != 10) # NotEquals
print(10 > 20)  # Greater than
print(10 < 20) # Lesser than
print(10 >= 10) # Greater than or equal
print(10 <= 20) # Lesser than or equal

print(".................")
# Logical Operators
print(1.0 == 1.2 and 10 < 20) # Logical
print(1.0 == 1.2 or 10 < 20)
print(not 10 < 10)

